# Example 1: A for loop that prints the elements of a list
list_of_numbers = [1, 2, 3, 4, 5]

print("Example 1: Printing elements of a list")
for element in list_of_numbers:
    print(element)
print()  # Print a newline for better separation of examples

# Example 2: A for loop that prints numbers from 0 to 9
print("Example 2: Printing numbers from 0 to 9")
for number in range(10):
    print(number)
