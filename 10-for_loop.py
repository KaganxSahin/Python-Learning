# Example 4: Calculating the square of each element in a list
numbers = [1, 2, 3, 4, 5]

print("Example 4: Calculating the square of each element in a list")
for number in numbers:
    print(number ** 2)
print()  # Print a newline for better separation of examples

# Example 5: Iterating over a dictionary
student_grades = {"John": 85, "Jane": 90, "Mike": 78}

print("Example 5: Iterating over a dictionary")
for student, grade in student_grades.items():
    print(f"{student}: {grade}")
print()  # Print a newline for better separation of examples
