# Using len() with a list
my_list = [1, 2, 3, 4, 5]
print("Length of my_list:", len(my_list))  # Output: 5

# Using len() with a string
my_string = "Python"
print("Length of my_string:", len(my_string))  # Output: 6

# Using len() with a tuple
my_tuple = (1, 2, 3)
print("Length of my_tuple:", len(my_tuple))  # Output: 3

# Using len() with a dictionary
my_dict = {"a": 1, "b": 2, "c": 3}
print("Length of my_dict:", len(my_dict))  # Output: 3

# Using len() with a set
my_set = {1, 2, 3}
print("Length of my_set:", len(my_set))  # Output: 3
